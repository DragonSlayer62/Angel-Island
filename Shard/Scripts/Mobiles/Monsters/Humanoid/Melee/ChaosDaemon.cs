using System;
using System.Collections;
using Server.Items;
using Server.Targeting;

namespace Server.Mobiles
{
	[CorpseName("a chaos daemon corpse")]
	public class ChaosDaemon : BaseCreature
	{
		[Constructable]
		public ChaosDaemon()
			: base(AIType.AI_Melee, FightMode.All | FightMode.Closest, 10, 1, 0.25, 0.5)
		{
			Name = "a chaos daemon";
			Body = 792;
			BaseSoundID = 0x3E9;

			SetStr(106, 130);
			SetDex(171, 200);
			SetInt(56, 80);

			SetHits(91, 110);

			SetDamage(12, 17);

			SetSkill(SkillName.MagicResist, 85.1, 95.0);
			SetSkill(SkillName.Tactics, 70.1, 80.0);
			SetSkill(SkillName.Wrestling, 95.1, 100.0);

			Fame = 3000;
			Karma = -4000;

			VirtualArmor = 15;
		}

		public ChaosDaemon(Serial serial)
			: base(serial)
		{
		}

		public override void GenerateLoot()
		{
			if (Core.UOAI || Core.UOAR)
			{
				PackGold(130, 170);
				// Category 2 MID
				PackMagicItem(1, 1, 0.05);
			}
			else
			{
				if (Core.UOSP || Core.UOMO)
				{	// LBR: we'll not need to worry about this guys loot unless we decide to add LBR
					// http://web.archive.org/web/20020414105125/uo.stratics.com/hunters/chaosdaemon.shtml
					// unknown
					if (Spawning)
					{
						PackGold(0);
					}
					else
					{
					}
				}
				else
				{
					AddLoot(LootPack.Average);
					AddLoot(LootPack.Meager);
				}
			}
		}

		public override void Serialize(GenericWriter writer)
		{
			base.Serialize(writer);
			writer.Write((int)0);
		}

		public override void Deserialize(GenericReader reader)
		{
			base.Deserialize(reader);
			int version = reader.ReadInt();
		}
	}
}
